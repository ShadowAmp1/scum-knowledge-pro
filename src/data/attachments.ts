export function getAttachmentBySlug(slug: string) {
  return attachments.find((attachment) => attachment.slug === slug);
}

export function getWeaponMatchStatus(attachment: Attachment) {
  const matched = attachment.compatibleWeaponSlugs
    .map((slug) => weapons.find((weapon) => weapon.slug === slug))
    .filter(Boolean);

  return {
    matchedCount: matched.length,
    totalListed: attachment.compatibleWeapons.length,
    matchedWeapons: matched,
    hasMatches: matched.length > 0,
  };
}
