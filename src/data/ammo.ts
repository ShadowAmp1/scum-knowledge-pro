// Helper functions
export const getAmmoByCaliber = (caliber: string): AmmoType[] => {
  return ammoTypes.filter(ammo => ammo.caliber === caliber);
};

export const getAmmoById = (id: string): AmmoType | undefined => {
  return ammoTypes.find(ammo => ammo.id === id);
};

export const getTargetById = (id: string): TargetType | undefined => {
  return targetTypes.find(target => target.id === id);
};

export const getWeaponByName = (name: string): WeaponStats | undefined => {
  return weaponStats.find(weapon => weapon.name === name);
};
