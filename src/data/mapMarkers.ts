function bunker(
  sector: string,
  x: number,
  y: number,
  risk: MapMarker["risk"] = "Высокий",
  tier: MapMarker["lootTier"] = "T4"
): MapMarker {
  return {
    id: `${sector.toLowerCase()}-bunker`,
    name: `Бункер ${sector}`,
    category: "bunker",
    sector,
    x,
    y,
    risk,
    lootTier: tier,
    short: `Высокотехнологичный бункер в секторе ${sector}.`,
    description:
      `Бункер ${sector}. Метка выставлена по текущей карте проекта. Это обычный высокотехнологичный бункер: снаружи обычно есть опасная зона, внутри военный лут, шкафчики, оружие, патроны и расходники.`,
    bestFor: ["Военный лут", "Оружие", "Патроны", "Броня", "Рейд"],
    recommendedKit: bunkerKit,
    linkedHref: `/bunkers/${sector.toLowerCase()}-regular-bunker`,
  };
}

